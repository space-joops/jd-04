// ============================================================================
// haptics.ts — 진동 피드백 (§10)
//
// navigator.vibrate는 지원 기기(주로 안드로이드 크롬)에서만 동작하고,
// iOS 사파리 등은 무시하거나 아예 API가 없다. 진동은 "있으면 좋은" 부가
// 기능이므로 미지원 환경에서 조용히 생략한다 — 게임을 절대 막지 않는다 (§12).
// ============================================================================

/** 짧은 진동 한 번. 먹기 12ms / 피격 90ms (§10) — 호출부가 길이를 정한다. */
export function vibrate(ms: number): void {
  try {
    navigator.vibrate?.(ms);
  } catch {
    // 미지원·권한 거부 — 무엇이든 조용히 넘어간다.
  }
}
